let currentQuestions = [];
let currentQuestionIndex = 0;
let score = 0;
let incorrectAnswers = [];

// Function to start the quiz for the selected week
function startQuiz(week) {
    currentQuestions = combinedWeeks[week] ? shuffleArray(combinedWeeks[week]) : shuffleArray(questions[week]);
    currentQuestionIndex = 0;
    score = 0;
    incorrectAnswers = [];
    document.getElementById("quizContainer").classList.remove("hidden");
    document.getElementById("resultContainer").classList.add("hidden");
    showQuestion();
}

// Utility function to shuffle the array (questions/options)
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// Function to display the current question
function showQuestion() {
    const questionContainer = document.getElementById("questionContainer");
    questionContainer.innerHTML = "";
    const question = currentQuestions[currentQuestionIndex];
    const questionElement = document.createElement("div");
    questionElement.classList.add("question");
    questionElement.innerText = question.question;
    const optionsList = document.createElement("ul");
    optionsList.classList.add("options");
    question.options.forEach((option, index) => {
        const li = document.createElement("li");
        li.innerHTML = `<input type="radio" name="option" value="${index}" id="option${index}">
                        <label for="option${index}">${option}</label>`;
        optionsList.appendChild(li);
    });
    questionContainer.appendChild(questionElement);
    questionContainer.appendChild(optionsList);
    const nextButton = document.getElementById("nextButton");
    nextButton.classList.remove("hidden");
}

// Function to handle moving to the next question
function nextQuestion() {
    const selectedOption = document.querySelector('input[name="option"]:checked');
    if (selectedOption) {
        const answerIndex = parseInt(selectedOption.value);
        const currentQuestion = currentQuestions[currentQuestionIndex];

        if (answerIndex === currentQuestion.answer) {
            score++;
        } else {
            incorrectAnswers.push({
                question: currentQuestion.question,
                selectedAnswer: currentQuestion.options[answerIndex], // Store the selected answer
                correctAnswer: currentQuestion.options[currentQuestion.answer],
                options: currentQuestion.options // Store options for display
            });
        }
        currentQuestionIndex++;

        if (currentQuestionIndex < currentQuestions.length) {
            showQuestion();
        } else {
            showResult();
        }
    } else {
        alert("Please select an answer before proceeding.");
    }
}

// Function to display the quiz results
function showResult() {
    document.getElementById("quizContainer").classList.add("hidden");
    document.getElementById("resultContainer").classList.remove("hidden");
    document.getElementById("score").innerText = `Your score: ${score} out of ${currentQuestions.length}`;

    const incorrectQuestionsList = document.getElementById("incorrectQuestions");
    incorrectQuestionsList.innerHTML = "";

    // Display incorrect answers
    incorrectAnswers.forEach(item => {
        const li = document.createElement('li');
        li.innerHTML = `<strong>${item.question}</strong><br>
                        <ul>
                          ${item.options.map((option, optionIndex) => {
                              const isCorrect = optionIndex === currentQuestions[currentQuestions.findIndex(q => q.question === item.question)].answer;
                              const isSelected = item.selectedAnswer === option;
                              let style = "";

                              if (isSelected && !isCorrect) {
                                  style = 'color: red;'; // Selected answer is incorrect
                              } else if (isCorrect) {
                                  style = 'color: green; font-weight: bold;'; // Correct answer
                              }

                              return `<li style="${style}">${option}</li>`;
                          }).join('')}
                        </ul>`;
        incorrectQuestionsList.appendChild(li);
    });
}

// Function to reset the quiz
function resetQuiz() {
    document.getElementById("resultContainer").classList.add("hidden");
    document.getElementById("quizContainer").classList.add("hidden");
}
